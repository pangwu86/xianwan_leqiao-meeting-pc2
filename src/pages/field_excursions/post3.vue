    <p>
      The loess in China is renowned for its wide distribution range,
      continuity, complete stratigraphic development, and large thickness,
      forming the famous Loess Plateau. The Quaternary loess in the Linxia
      Basin, like the typical sequence of the Loess Plateau, includes the Early
      Pleistocene Wucheng Loess, the Middle Pleistocene Lishi Loess, and the
      Late Pleistocene Malan Loess. The loess strata contain multiple paleosol
      layers, indicating that the ancient climate during the formation of loess
      underwent multiple dry-cold and humid-warm changes. Through the study of
      loess, the changes in atmospheric circulation, the evolution of ancient
      climate and environment, and the processes of dust transport and
      accumulation can be revealed. Therefore, loess, along with ice cores and
      deep-sea sediments, are known as the &ldquo;three pillars&rdquo; of paleo
      climatological research.
