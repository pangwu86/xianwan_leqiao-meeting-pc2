      Upper Devonian and Lower Carboniferous succession. Herein, the cone-shaped
      hills consist of Upper Devonian carbonates, which usually develop platform
      facies metazoan reefs (i.e. sponge and coral reefs) of Frasnian and
      microbialites of Famennian, witnessing biosedimentary changes from the
      metazoan-dominated reef systems to microbial reefs across the
      Frasnian-Famennian (F-F) mass extinction on shallow platforms.&nbsp;
    </p>
    <p>
      The continuous F-F boundary is recorded at the Yangdi and Baisha sections,
      representing the restricted platform and intraplatform basin facies,
      respectively. In Yangdi the F-F succession comprises the Xiangtian and
      Wuzhishan formations. The former is composed of grey to dark grey
      brecciated limestone, muddy limestone, nodular limestone, while the
      overlying Wuzhishan Formation comprises light grey nodular limestone. The
      Baisha section records relatively deeper successions of the F-F boundary,
