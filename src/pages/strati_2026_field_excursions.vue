    <h3>
      5.&nbsp;<strong
        ><strong>Linxia Basin and Loess in North China</strong></strong
      >
    </h3>
    <p>
      The Linxia Basin is located at the triple junction of the northeastern
      Tibetan Plateau, the western Qinling Mountains, and the Loess Plateau,
      delineated by high-angle deep thrusts. The lateral extent of the Linxia
      Basin is marked by structural boundaries on the northern, western, and
      southern edges, but its eastern margin is poorly determined. The basin is
      filled with 700-2000 m of Cenozoic deposits, mainly red in color and
      dominated by lacustrine siltstones and mudstones punctuated by fluvial
      conglomerates or sandstones, and 30-200 m of Quaternary loess sediments.
      To the west and south, the major basin-bounding faults within the Tibetan
      Plateau are the Leijishan and North Qinling faults, respectively. The
      Cenozoic deposits of the Linxia Basin begin in the Eocene and lap over
      Cretaceous deposits in the Maxian Mountains to the north. Throughout the
      central part of the Linxia Basin, the oldest deposits were laid down
      unconformably on the granite of presumed Paleozoic age. To the southwest
      of the Linxia Basin, the Tibetan Plateau consists of Devonian-Permian
      terrestrial and marine deposits and Triassic submarine fan deposits, which
      were shed by the east-southeast striking Qinling Mountain belt to the east
      of the plateau.
    </p>
    <p>
      <img src="../imgs/field_excursions/042324744.png" alt="">
    </p>
    <p class="text-center">
      An example of the Linxia Basin exposures is near the basin center looking
      toward the northwest, and these Cenozoic exposures are excellent and
      continuous, with many fossil-rich beds.
    </p>
    <p>
      The Cenozoic sediments in the Linxia Basin are relatively stable, with
      strata close to horizontal and containing a large number of mammalian and
      other vertebrate fossils. It is not only an ideal area for studying the
      evolution of Cenozoic mammals, but also records important information such
      as the tectonic movement and climate change in the northeastern margin of
      the Tibetan Plateau. The localities in the Linxia Basin are notable for
      abundant, relatively complete, well-preserved, and sometimes partially
      articulated bones of large mammals, which often occur in dense
      concentrations. Many new species of the Late Oligocene
      <em><em>Paraceratherium </em></em>(giant rhino) fauna, the Middle Miocene
      <em><em>Platybelodon </em></em>(shovel-tusked elephant) fauna, the Late
      Miocene <em><em>Hipparion </em></em>(three-toed horse) fauna, and the
      Early Pleistocene <em><em>Equus</em></em
      >&nbsp;(true horse)<em><em>&nbsp;</em></em
      >fauna have been described from the Linxia Basin, including insectivores,
      rodents, lagomorphs, primates, carnivores, proboscideans, perissodactyls,
      and artiodactyls. The region in the Linxia Basin, mainly consisting of
      Hezheng, Guanghe, and Dongxiang counties, which is rich in Cenozoic
      fossils, is called the Hezheng area. It is the largest Cenozoic mammalian
      fossil producing area in China and the world, especially in the period of
      the <em>Hipparion</em>&nbsp;fauna.
    </p>
    <p>
      The loess in China is renowned for its wide distribution range,
      continuity, complete stratigraphic development, and large thickness,
      forming the famous Loess Plateau. The Quaternary loess in the Linxia
      Basin, like the typical sequence of the Loess Plateau, includes the Early
      Pleistocene Wucheng Loess, the Middle Pleistocene Lishi Loess, and the
      Late Pleistocene Malan Loess. The loess strata contain multiple paleosol
      layers, indicating that the ancient climate during the formation of loess
      underwent multiple dry-cold and humid-warm changes. Through the study of
      loess, the changes in atmospheric circulation, the evolution of ancient
      climate and environment, and the processes of dust transport and
      accumulation can be revealed. Therefore, loess, along with ice cores and
      deep-sea sediments, are known as the &ldquo;three pillars&rdquo; of
      paleoclimatological research.
    </p>
