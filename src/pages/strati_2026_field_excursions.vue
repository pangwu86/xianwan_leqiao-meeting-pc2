      section, but with a much thicker Kayitou Formation. Fossil plants are
      commonly seen in the Xuanwei and Kayitou formations. Recently, a
      ~300-m-long drill core has been obtained from this section, assisting a
      better understanding of the palaeoenvironmental and palaeoclimatical
      changes across the critical Permian&ndash;Triassic transition. Relevant
      geochemical and geophysical studies are ongoing.
