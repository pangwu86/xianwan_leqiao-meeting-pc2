    <h3>
      3.&nbsp;<strong
        ><strong
          >Ordovician to Silurian strata and Anji Biota in the Anji,
          Zhejiang</strong
        ></strong
      >
    </h3>
    <p>
      The Hanggai section in Anji, Northwestern Zhejiang Province, was ratified
      as the &ldquo;Reference Section of the Hirnantian Stage for Lower Yangtze
      Region, South China&rdquo;. The section exposes continuous Upper Katian to
      Rhuddanian interval of graptolitic shale and fine grain clastics deposits,
      in which the Hirnantian Stage is ca. 360 m thick (Wenchang Formation). The
      section contains fairly diverse fossils, including abundant graptolites,
      chitinozoans, and sponges, and less abundant brachiopods, trilobites and
      gastropods, etc. Biostratigraphically, six successive graptolite biozones
      and a benthic assemblage are identified.
    </p>
    <p>
      The Anji Biota reported here is a unique deposit with exceptionally
      preserved sponges and other organisms in deep-water, uppermost Ordovician
      mudstone. The sponge fossils preserve remarkable details
      including&nbsp;carbonaceous preservation of soft tissues and axial
      filaments of spicules. The discovery of Anji Biota, dominated by abundant
      and diverse sponges and graptolite in the Hirnantian interval opens a new
      window to probe into the marine benthic community during the Hirnantian
      Extinction and shed lights on the sponge evolution after the Cambrian
      Explosion.
    </p>
