    <h3>
      6.&nbsp;<strong
        ><strong
          >Mesoproterozoic, Cambrian and terrestrial Permian-Triassic
          successions in the Yuntai Mountain-Shaolin Temple areas, Henan
          Province, central China</strong
        ></strong
      >
    </h3>
    <p>
      Marine Mesoproterozoic and Cambrian, and terrestrial Permian and Triassic
      successions are well exposed in the Yuntaishan, Dengfeng and Yiyang areas,
      respectively in Henan Province, central China. The Yuntai mountain
      consists mainly of Mesoproterozoic conglomerate, sandstone and mudstone of
      the Ruyang Group. The spectacular reddish sandstone landscapes sculptured
      by the local creeks not only attract publics and thus become one of the
      most attractive tourist hot spots in central China, but also provide a
      unique window investigating Mesoproterozoic environment and ecosystems.
      Here, reddish sandstone yields abundant microbially induced sedimentary
      structures (MISSs), and wrinkle structures, sandy cracks and microbial
      mats are particularly common. Acritarch microfossils have also extracted
      from the mudstone sediments of the Ruyang Group.
    </p>
    <p>
      Cambrian successions are well exposed in the Dengfeng area, at which the
      famous Shaolin Temple of Chinese Kung Fu is located. The Cambrian strata
      are dominated by muddy limestones which are often highly bioturbated and
      yield abundant microbialites and giant ooids. Here we will examine
      substrate revolution of Cambrian where trace-making animals were
      associated with microbial ecosystems (microbial mats or ooids).
      Terrestrial Permian and Triassic successions are well exposed in many
      areas of Henan Province, while the Permian-Triassic boundary succession is
      best exposed in Yiyang area. Here, the uppermost Permian strata comprise
      greenish and black shale/mudstone and sandstone of floodplain facies of
      the lower Sunjiagou Formation, while the basal Triassic is characterized
      by massive reddish paleosols, sandstone and mudstone of the upper
      Sunjiagou Formation, followed by massive sandstone/conglomerates of the
      Liujiagou Formation, which indicates the sedimentation of fluvial facies.
      The lithological variations are distinct across the P-Tr boundary and also
      indicate pronounced climatic changes from warm-wet condition to arid-hot
      regime.
    </p>
