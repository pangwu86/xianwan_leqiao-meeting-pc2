    <h3>
      2.&nbsp;<strong><strong>Tai Lake Xishan Section</strong></strong>
    </h3>
    <p>
      &nbsp;The Xishan section is located on the Xishan Island in Tai Lake in
      Jiangsu Province, South China. During the Permian-Triassic transition, the
      South China Block was located in the eastern Palaeo-Tethys near the
      equator, where shallow platforms were widespread and separated by numerous
      deep intra-platform basins or troughs. The section is composed of the
      uppermost Permian and the lowermost Triassic strata. The uppermost Permian
      succession (Changxing Formation) is a set of 179 m-thick limestone
      accumulated on the carbonate platform. These limestones mainly comprise
      thick- to very thick-bedded bioclastic grainstone/packstone with sponge
      reef/mound interbeddings. Overlying the grainstone/packstone-reef/mound
      complex is a set of 9 m-thick thin- to medium-bedded calcareous algae
      wackestone/packstone. Fossils in the Permian Changxing Formation are
      abundant and diverse, including calcareous algae, calcareous sponges,
      bryozoans, crinoids, fusulinids, small foraminifers, gastropods,
      brachiopods, and ostracods. The lowermost Triassic succession (Yinkeng
      Formation) is a 14-m-thick deep-shelf mixed carbonate-siliciclastic
      sequence, consisting of grayish green and grayish black mudstone with very
      thin- to thin-bedded dolomite interbeddings. Fossils are sparse, dominated
      by brachiopods, gastropods, and echinoderm clasts of Permian types, with
      occasional Triassic-type bivalve shell beds. The abrupt decrease of fossil
      diversity and abundance at the boundary between the Changxing and the
      Yinkeng formations indicates the Permian-Triassic event boundary (or the
      maximum mass extinction horizon).
    </p>
    <p>
      <img src="../imgs/field_excursions/042338926.png" alt="">
    </p>
    <p class="text-center">The Permian-Triassic boundary interval at Xishan, Suzhou</p>
    <p>
      The basal 2-m-thick layer of the Yingkeng Formation is a dolostone crust.
      It is in this stratigraphic interval we discovered carbonate concretions
      approximately 10 cm across with fabrics and fossils associated with
      microbialites. The microbialite-associated structures in these concretions
      include clotted fabrics and laminated carbonate needles, as well as
      abundant carbonate crystal fans. Fossil groups associated with
      microbialite include microconchids, small gastropods, and small
      foraminifers. On the basis of the analysis of the microfabrics and the
      fossil groups together with a comparison to modern analogues, we attribute
      the formation of the micritic patches in the microbialite concretions to
      the calcification of cyanobacterial mats via carbonate nanoparticles and
      we attribute the carbonate crystal fans to the direct recrystallization of
      micritic carbonates. The sparitic patches were interpreted as either the
      direct recrystallization of micritic carbonates or the precipitation of
      carbonate spars in the inter-/intra-spaces of metazoan shells together
      with the recrystallization of these shells. The similarities to modern
      stromatolites, both in morphology and in internal texture, suggest that
      the laminated carbonate needles are stromatolite laminae built by
      filamentous cyanobacteria. The preservation of these microbialite
      microfabrics indicates that early lithification by carbonate precipitation
      was widespread and intense following the end-Permian boundary events. The
      weak development of microbialites as small concretions may be attributed
      to the deeper water depth and the lower water energy in the Xishan area
      during the earliest Triassic.
    </p>
