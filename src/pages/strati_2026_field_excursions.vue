    <h3>
      3.&nbsp;<strong
        ><strong
          >From Snowball Earth to Great Mass Extinction: Ediacaran to Triassic
          Stratigraphic Records in the Three Gorge area, central China</strong
        ></strong
      >
    </h3>
    <p>
      The Three George area, western Hubei Province, central China is not only
      famous with the spectacular landscapes sculptured by the Yangtze River and
      the greatest dam in the world, but also records one of the most continuous
      marine Late Neoproterozoic to Mesozoic successions in China, providing the
      classic book of Earth evolution for this period and displaying excellent
      stratigraphic and geologic records of several most important events
      characterizing the life-environment evolutions of Earth, including the
      Snowball Earth event, early animal evolution, Cambrian explosion
      (Qingjiang Lagerst&auml;tte), the Great Ordovician Biodiversification,
      end-Ordovician mass extinction, two GSSPs of stages within the Ordovician,
      middle-late Permian mass extinction, Permian-Triassic mass extinction, and
      Early Triassic hothouse regime.
    </p>
    <p>
       <img src="../imgs/field_excursions/4img.jpg" alt="">
    </p>
    <p class="text-center">
      Photos showing cap carbonate of the Doushantuo Formation, Ediacaran (upper
      left), the beautiful landscape of the Dengying Fm dolomites sculptured by
      Yangte River (upper right), water highway in Three Gorge area (lower
      left), and the Great Dam of the Yangtze River (lower right).
    </p>
    <p>
      This 7-day excursion will examine Ediacaran succession that is
      characterized by Marinoan tillite, cap carbonate, black shale, and massive
      dolomite, and correlates well with their counterparts exposed elsewhere in
      China and the world. The well-known Marinoan Snowball Earth event and its
      aftermath as well as unique records of well-known multicellular Ediacaran
      biota (Shibantan Biota, including the first footprint of animal ever in
      Earth history) are also investigated in this route. The
      Precambrian-Cambrian transition is rather continuous and comprised of
      black shales containing abundant phosphorus sediments of industrial value.
      The Qingjiang Lagerst&auml;tte, equivalent to the Chengjiang Biota, from
      the Changyang county of Yichang City witnessed the Cambrian explosion in
      early Cambrian. The Lower Paleozoic successions are particularly
      continuous around the Yichang City and yield abundant invertebrate
      fossils. To date, at least two stage-level GSSPs of the Ordovician have
      been established in this area, and the Ordovician/Silurian (O/S) boundary
      mass extinction is also well recorded herein. The equivalent of black
      shale succession yielding huge sources of shale-gas in South China is also
      examined during this excursion. The Devonian to Lower Permian strata are
      locally exposed in the Three Gorge area. The middle Permian to Triassic
      successions are well exposed in the Xiaokou section of the Xingshan
      county, northern part of the Three Gorge area, displaying lithologic and
      biotic variations across the Guadalupian-Lopingian and Permian-Triassic
      mass extinctions. The Lower marine reptile Lagerst&auml;tte of the Yuanan
      Biota is also located in the north of Yichang City. The Middle-Upper
      Triassic successions are dominated by lacustrine facies sediments, while
      the Jurassic comprises fluvial facies conglomerate, sandstone and mudstone
      due to the uplift of the Three Gorge area since the Middle Triassic.
    </p>
    <p>
      The complete and continuous successions of the Ediacaran to Triassic of
      this excursion offers you a better understanding of biotic and
      environmental evolution during the first half pace of Phanerozoic.
      Additionally, this field excursion provides a unique opportunity to enjoy
      the spectacular landscapes of the Three Gorges and great dam as well as
      local cultures of ancient Chu Kingdom in the central part of China.
    </p>
