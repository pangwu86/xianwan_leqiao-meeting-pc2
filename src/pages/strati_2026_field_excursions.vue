    <h3>
      7.&nbsp;<strong
        ><strong
          >Upper Devonian to lowest Carboniferous successions in Guilin Karst
          area, Guangxi, South China</strong
        ></strong
      >
    </h3>
    <p>
      <img src="../imgs/field_excursions/042330938.png" alt="">
    </p>
    <p class="text-center">
      Field photos showing spectacular kart landscapes of Upper Devonian
      microbial carbonates in Guilin area, Guangxi Province, South China
    </p>
    <p>
      The Guilin area of Guangxi Province, South China is famous for its
      spectacular carbonate karst landscapes and fantastic carbonate caves.
      Guilin, with diversified depositional facies, including restricted
      carbonate platform interior, open platform-platform margin, and marginal
      slope-basinal facies, locally rich in marine fauna (e.g., brachiopods,
      corals, bryozoans,bivalves, trilobites, conodonts and ostracods), is one
      of the potential areas suitable for neritic-pelagic correlation of the
      Upper Devonian and Lower Carboniferous succession. Herein, the cone-shaped
      hills consist of Upper Devonian carbonates, which usually develop platform
      facies metazoan reefs (i.e. sponge and coral reefs) of Frasnian and
      microbialites of Famennian, witnessing biosedimentary changes from the
      metazoan-dominated reef systems to microbial reefs across the
      Frasnian-Famennian (F-F) mass extinction on shallow platforms.&nbsp;
    </p>
    <p>
      The continuous F-F boundary is recorded at the Yangdi and Baisha sections,
      representing the restricted platform and intraplatform basin facies,
      respectively. In Yangdi the F-F succession comprises the Xiangtian and
      Wuzhishan formations. The former is composed of grey to dark grey
      brecciated limestone, muddy limestone, nodular limestone, while the
      overlying Wuzhishan Formation comprises light grey nodular limestone. The
      Baisha section records relatively deeper successions of the F-F boundary,
      which are dominated by thin-bedded laminated muddy limestone of the Gubi
      Formation at the lower part of the section, while the upper part also
      consists of the Wuzhishan Formation (Zhao et al., 2024).The
      Famennian-Tournaisian (also Devonian-Carboniferous) boundary succession is
      recorded in the north of Guilin, the Nanbiancun section, which has been
      recommended by the International Working Group of D/C Boundary as an
      auxiliary stratotype of the Devonian-Carboniferous boundary in 1989.
      Besides, GSSP for the base of the Visean Stage has been established in the
      Pengchong section, Liuzhou, which is near the Guilin City.
    </p>
    <p>
      During this field excursion, you will not only have some basic knowledge
      of typical Chinese Devonian and Lower Carboniferous in terms of
      stratigraphy and sedimentology, but also enjoy the spectacular karstic
      scenery around Guilin area.
    </p>
