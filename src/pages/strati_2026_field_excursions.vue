    <p>
      This section, along a country road to a water reservoir, includes the
      middle&ndash;upper Permian Emeishan Basalts, upper Permian&ndash;Lower
      Triassic coal-bearing Xuanwei Formation, Lower Triassic Kayitou Formation,
      and Lower Triassic Dongchuan Formation. Diverse fossil plants of the
      Cathaysian flora, including lycophytes, ferns and seed-ferns (i.e.
      sphenophytes, pecopterids), gigantopterids, cycadophytes
      (<em>Pterophyllum</em>), and primitive ginkgophytes are common in the
      Xuanwei and Kayitou formations. Ash-beds are developed in the Xuanwei and
      Kayitou formations. Dongchuan Formation is a red-bed sequence.
