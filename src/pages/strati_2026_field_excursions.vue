    <h3>
      2.&nbsp;<strong
        ><strong
          >Late Paleozoic to earliest Mesozoic stratigraphy and faunas in
          Guizhou, South China
        </strong></strong
      >
    </h3>
    <p>
      This excursion investigates the well-preserved successions of
      Carboniferous, Permian to early Triassic, including: the Late Paleozoic
      ice age (LPIA) at the Naqing section, and the Early Triassic
      lagerst&auml;tten of the Wangmo and Guiyang biotas.
    </p>
    <p>
      The Naqing section, is located in Luodian County, southern Guizhou
      province, South China. It is represented by the platform margin to slope
      facies and contains continuous carbonate deposits from the Early
      Carboniferous to the Late Permian. Conodont fossils are abundant
      throughout the section, providing potential correlation with the global
      scale chronostratigraphic scheme. The basal boundary of four Carboniferous
      stages, namely the Serpukhovian, Moscovian, Kasimovian, and Gzhelian
      stages can be well defined by conodonts and foraminifers (fusulines) in
      the section. Meanwhile, through the geochemical studies on
      &delta;<sup>238</sup>U<sub>carb</sub>,
      &delta;<sup>13</sup>C<sub>carb</sub>,
      and<sub>&nbsp;</sub>&delta;<sup>18</sup>O<sub>PO4</sub>, two environmental
      events such as warming and expanded oceanic anoxia during the Late
      Paleozoic Ice Age (LPIA) can be observed in this section, one of which
      occur at the end-Serpukhovian stage and another at the earliest Gzhelian
      stage.
    </p>
    <p>
      High-resolution cyclostratigraphy and sedimentology further enhance our
      understanding of the temporal and spatial evolution of the South China Sea
      during the LPIA. The Naqing section offers a unique opportunity to refine
      the Carboniferous and Lower Permian timescales, study the evolution of
      Late Paleozoic conodonts, reconstruct paleoenvironmental and paleoclimatic
      changes during the LPIA, and examine the bio-environment interactions
      during one of Earth's most extreme climatic periods.
    </p>
    <p>
      <img src="../imgs/field_excursions/042313986.png" alt="">
    </p>
    <p class="text-center">
      Distal view of the Naqing section with a sequence from Devonian until
      Triassic
    </p>
    <p>
      <strong><strong>&nbsp;</strong></strong>
    </p>
    <p>
      The exceptionally preserved lagerst&auml;tten have also been reported from
      the Wangmo county and Guiyang city, Guizhou Province, named as the Wangmo
      and Guiyang biotas. These exceptional fossils are yielded in the lower
      part of the Lower Triassic (i.e. 250.83 +0.07/ &ndash; 0.06 million years
      ago). The Guiyang biota comprises at least 12 classes and 19 orders,
      including diverse fish fauna and malacostracans, revealing a trophically
      complex marine ecosystem. Therefore, these early Triassic
      lagerst&auml;tten represent the oldest known Mesozoic biotas, marking the
      rapid rise of modern-type marine ecosystems after the Permian-Triassic
      mass extinction. The Wangmo biota is an another newly discovered early
      Triassic fossil Lagerst&auml;tte in Wangmo County, two hours driving from
      Guiyang. The fossils are exceptionally preserved in thin bedded mudstones
      and microcrystalline limestones of the Lower Triassic Luolou Formation.
      The fossil assemblages are dominated by ammonites and arthropods,
      accompanied by bivalves, belemnoids, brachiopods, conodonts, fishes,
      microconchids, and problematic fossils.
    </p>
