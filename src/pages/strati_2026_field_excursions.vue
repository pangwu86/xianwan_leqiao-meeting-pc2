    <h3>
      8.&nbsp;<strong
        ><strong
          >Mesoproterozoic successions in Jixian area, Tianjin City, North
          China</strong
        ></strong
      >
    </h3>
    <p>
      <img src="../imgs/field_excursions/042333892.png" alt="">
    </p>
    <p class="text-center">
      Field photos showing carbonate successions of Mesoproterozoic in Jixian,
      Tianjin City
    </p>
    <p>
      The relatively complete Mesoproterozoic successions were deposited in some
      rift and post-rift basins in the North China Craton in the course of
      tectonic evolution from the break-up of Columbia to the assembly of
      Rodinia during the Mesoproterozoic times. The entire Mesoproterozoic
      succession is composed of the Changcheng and Jixian Groups. The former
      includes the Tuanshanzi, Dahongyu, and Gaoyuzhuang Formations, while the
      Jixian Group comprises the Yangzhuang, Wumishan, Hongshuizhuang, and
      Tieling Formations. The overlying Xiamaling Formation is assigned to
      Tonian in age.
    </p>
    <p>
      The Mesoproterozoic successions (~6.4 Ga-1.4 Ga) are best exposed in the
      Jixian area, which was situated the northeastern margins of the North
      China Craton. This 2-day excursion will examine the Changzhougou,
      Chuanlinggou, Tuanshanzi, Dahongyu, Gaoyuzhuang, Yangzhuang, Wumishan,
      Hongshuizhuang, Tieling, and Xiamaling formations in ascending order. Of
      these, the first confirmed eukaryotes from the Gaoyuzhuang Formation (Miao
      et al., 2024), mini-digital stromatolites from the Wumishan Formation, and
      spectacular giant stromatolites from the Tieling Formation are most
      important attractions in this route.
    </p>
    <p>
      <img src="../imgs/field_excursions/042335167.png" alt="">
