    <h3>
      4.&nbsp;<strong
        ><strong>The Yanliao and Jehol biotas in North China</strong></strong
      >
    </h3>
    <p>
      The Mesozoic strata of western Liaoning, northern Hebei&nbsp;Province, and
      southeastern Inner Mongolia host the renowned Yanliao and Jehol Biotas,
      offering critical insights into terrestrial ecosystems during the middle
      to late Mesozoic.
    </p>
    <p>
      The Yanliao Biota is widely distributed in Ningcheng, Inner Mongolia;
      Luanping and Qinglong in northern Hebei; and Lingyuan, Beipiao, Jianchang,
      and Jianping in western Liaoning. It can be subdivided into two
      assemblages: the Lower Assemblage, represented by the Daohugou Biota, with
      fossils preserved in the Haifanggou Formation (upper Middle Jurassic) in
      Ningcheng, Inner Mongolia, where numerous fossils of insects,
      branchiopods, salamanders, feathered dinosaurs, diverse mammals, and
      abundant plant fossils have been discovered; and the Upper Assemblage,
      represented by the Linglongta Biota, with fossils preserved in the
      Tiaojishan Formation (Middle to Upper Jurassic) in Jianchang, western
      Liaoning, where numerous fossils of insects, fishes, feathered dinosaurs,
      and pterosaurs have been found.
    </p>
    <p>
      The Early Cretaceous Jehol Biota is widely distributed in Beipiao and
      Lingyuan in western Liaoning, Weichang and Pingquan in northern Hebei, and
      Ningcheng in Inner Mongolia. It can be divided into three assemblages: the
      Lower Assemblage, mainly preserved in the Dabeigou Formation in northern
      Hebei, yielding numerous fossils of insects, branchiopods, fishes,
      salamanders, and birds; the Middle Assemblage, marking the zenith of the
      flourishing period of the Jehol Biota, centered in the Yanliao area and
      preserved in the Yixian Formation, characterized by abundant fossils of
      insects, fishes, pterosaurs, dinosaurs, birds, mammals, and plants, among
      which the <em><em>Confuciusornis</em></em
      >-rich layer has an absolute age of <em><em>ca</em></em
      >. 125 Ma; and the Upper Assemblage, represented by fossils preserved in
      the Jiufotang Formation in Lingyuan and Jianchang, western Liaoning,
      mainly including fishes, reptiles, and birds.
    </p>
    <p>
      The exceptionally preserved Yanliao Biota of the Middle-Late Jurassic and
      the Jehol Biota of the Early Cretaceous are located in core areas with
      developed volcanic rocks, and their fossil layers often have
      well-constrained chronologies. The fossils are exquisitely preserved and
      vast in number, serving as a treasure trove for understanding the
      evolution of terrestrial ecosystems during the Mesozoic.
    </p>
