    </h3>
    <p>
      This <strong><strong>7</strong></strong
      ><strong><strong>-day excursion</strong></strong
      >&nbsp;will be organized to check the Cryogenian-Cambrian sections and
      exceptionally preserved fossil deposits in various facies ranging from the
      shallow to deeper water, including famous Burgess Shale-type fossil sites
      and the Cambrian GSSPs of the Cambrian Wuliuan, Guzhangian and Paibian
      stages in Guizhou and W. Hunan. &nbsp;&nbsp;
    </p>
    <p>&nbsp;</p>
    <p>
      The main geological cites include: (1) The Cryogenian-Ediacaran section
      and the fossil site of the Ediacaran Weng&rsquo;an biota at Beidoushan
      phosphorite mine, Weng&rsquo;an, showing the abundant and diverse
      phosphatized embryo fossils. (2) The&nbsp;pre-Cryogenian to Cambrian
      succession in the slope facies at Wuhe section,&nbsp;the Wuliuan
      GSSP&nbsp;and&nbsp;a potential GSSP of the unnamed Cambrian Stage 4, and
      the fossil sites&nbsp;of the Cambrian&nbsp;Balang and Kaili
      biotas&nbsp;in&nbsp;Jianhe. (3) The Cryogenian to Cambrian succession in
      the slope facies, and fossil site of the Ediacaran Wenghui biota showing
      the abundant and diverse macroscopic carbonaceous fossils associated with
      the Ediacara-type fossils at Taoying and the Cambrian Huayuan biota
      showing the abundant and diverse Burgess Shale-type fossils
      at&nbsp;Huayuan. (4) The GSSPs of the Cambrian Guzhangian and Paibian
      stages in Guzhang and&nbsp;Huayuan.
    </p>
