    <p>
      This 7-days&rsquo; excursion will examine several well-studied geological
      sections across the Ediacaran&ndash;Cambrian, Silurian&ndash;Devonian, and
      Permian&ndash;Triassic boundaries, and associated fossils.
    </p>
    <p>
      The main geological sites include: 1) The Ediacaran&ndash;Cambrian
      boundary at Meishucun section, Jinning District, Kunming City, showing the
      abundant and diverse phosphatized small shelly fossils and trace fossils,
      as well as the earliest occurrence of trilobites and soft-bodied fossils.
      2) The UNESCO Natural Heritage fossil site, the early Cambrian Chengjiang
      fauna, and the Field Research Station of Nanjing Institute of Geology and
      Palaeontology, Chinese Academy of Sciences, at Maotianshan, Chengjiang
      City, Yuxi City. 3) Sections across the Silurian&ndash;Devonian boundary
      and the famous early vertebrate fossil sites including the late Silurian
      Xiaoxiang fauna and Xitun fauna, as well as some early vascular land plant
      fossil sites in the vicinity of Qujing City. 4) Terrestrial
      Permian&ndash;Triassic sections at Taoshujing and Guanbachong, Zhaotong
      City, showing the transition from humid swamp to semiarid alluvial
      environments and the collapse of the tropical forest ecosystems across the
      Permian&ndash;Triassic&nbsp;boundary.
    </p>
